void Draw4816WithMinus(int row, int32_t num)
{
	uint8_t T_hi, T_lo, T_dot;
	bool Minus = false;
	if(num >= 999){
		T_hi = T_lo = T_dot = 9;
		Minus = false;
	} else if (num <= -999) {
		T_hi = T_lo = T_dot = 9;
		Minus = true;
	} else {
		if(num < 0) {
			Minus = true;
			num = 0 - num;
		}

		T_hi = (num / 100);
		T_lo = ((num /10 )% 10);
		T_dot = (num % 10);
	}

	if (T_hi == 0) T_hi = 10;

	Draw4816NumWithMinus(row, Minus, T_hi, T_lo);

	Draw4816DotNum(row, 2, T_dot);
}

